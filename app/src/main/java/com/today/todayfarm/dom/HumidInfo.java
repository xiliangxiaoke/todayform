package com.today.todayfarm.dom;

public class HumidInfo {

	int himidid;
	int humidtime;
	int fieldid;
	String path;
	double eleft;
	double ebottom;
	double eright;
	double etop;


	public int getHimidid() {
		return himidid;
	}
	public void setHimidid(int himidid) {
		this.himidid = himidid;
	}
	public int getHumidtime() {
		return humidtime;
	}
	public void setHumidtime(int humidtime) {
		this.humidtime = humidtime;
	}
	public int getFieldid() {
		return fieldid;
	}
	public void setFieldid(int fieldid) {
		this.fieldid = fieldid;
	}
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}
	public double getEleft() {
		return eleft;
	}
	public void setEleft(double eleft) {
		this.eleft = eleft;
	}
	public double getEbottom() {
		return ebottom;
	}
	public void setEbottom(double ebottom) {
		this.ebottom = ebottom;
	}
	public double getEright() {
		return eright;
	}
	public void setEright(double eright) {
		this.eright = eright;
	}
	public double getEtop() {
		return etop;
	}
	public void setEtop(double etop) {
		this.etop = etop;
	}


}

package com.today.todayfarm.dom;

public class GeoPoint {
    double lng;
    double lat;

    public double getLon() {
        return lng;
    }

    public void setLon(double lon) {
        this.lng = lon;
    }

    public double getLat() {
        return lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }
}
